import Head from "next/head";
import { selectShowModal } from "../redux/slice/authSlice";
import {
  selectSearchValue,
  FIRST_SONG,
  selectSearchSubmit,
} from "../redux/slice/songSlice";
import { useSelector, useDispatch } from "react-redux";
import { useEffect, useState } from "react";
import { onAuthStateChanged } from "firebase/auth";
import { auth } from "../firebase/config";
import { options } from "../utils/urlOptions";
import {
  Feeds,
  Header,
  Sidebar,
  SignupFooter,
  SignupModal,
  LoggedFeeds,
  Player,
} from "../components";

export async function getServerSideProps() {
  const newReleases = await fetch(
    "https://shazam.p.rapidapi.com/charts/track?locale=en-US&pageSize=20&startFrom=0",
    options
  ).then((data) => data.json());

  return {
    props: {
      newReleases,
    },
  };
}

export default function Home({ newReleases }) {
  const showModal = useSelector(selectShowModal);
  const searchValue = useSelector(selectSearchValue);
  const searchSubmit = useSelector(selectSearchSubmit);
  const dispatch = useDispatch();
  const [loading, setLoading] = useState(true);
  const [playlists, setPlaylists] = useState(newReleases);
  const [user, setUser] = useState(null);

  // handle search result
  useEffect(() => {
    const fetchSearchValue = async () => {
      if (searchValue) {
        const searchResult = await fetch(
          `https://shazam.p.rapidapi.com/search?term=${searchValue}%20the%20rain&locale=en-US&offset=0&limit=50`,
          options
        );
        const response = await searchResult.json();
        setPlaylists(response);
        console.log("response", response);
      }
    };
    fetchSearchValue();
  }, [searchSubmit]);

  // check user auth state
  useEffect(() => {
    onAuthStateChanged(auth, (user) => {
      if (user) {
        // User is signed in, see docs for a list of available properties
        setUser(user);
      }
      setLoading(false);
    });
    dispatch(FIRST_SONG(playlists?.tracks[0]));
  }, []);

  if (loading) return;

  return (
    <>
      <Head>
        <title>Home - Spotify</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        {/* Sidebar */}
        <Sidebar
          user={user}
          setPlaylists={setPlaylists}
          newReleases={newReleases}
        />

        {/* Header */}
        <Header
          user={user}
          setPlaylists={setPlaylists}
          newReleases={newReleases}
        />

        {/***** User logged out Feeds *****/}
        {!user && <Feeds user={user} />}

        {/***** User logged in Feeds *****/}
        {user && <LoggedFeeds playlists={playlists?.tracks} />}

        {/* Signup Footer Component */}
        {!user && <SignupFooter />}

        {/* Music Player */}
        {user && <Player />}

        {/* Signup Modal */}
        {showModal && <SignupModal />}
      </main>
    </>
  );
}
